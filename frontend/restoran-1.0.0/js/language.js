function applyLanguage(lang) {
    localStorage.setItem("currentLanguage", lang);
    const langLabel = document.getElementById("lang-label");
    if(langLabel) langLabel.textContent = lang.toUpperCase();

    // 1. Dịch các thẻ chứa nội dung text tĩnh (bao gồm cả label)
    document.querySelectorAll("[data-i18n]").forEach(element => {
        const key = element.getAttribute("data-i18n");
        if (dictionary[lang] && dictionary[lang][key]) {
            const translation = dictionary[lang][key];

            // Dịch nội dung hiển thị
            if (translation.includes("<")) {
                element.innerHTML = translation;
            } else {
                element.textContent = translation;
            }

            // Đồng bộ dịch placeholder tĩnh (dành cho các form Bootstrap thông thường)
            const inputId = element.getAttribute("for");
            if (inputId) {
                const targetInput = document.getElementById(inputId);
                if (targetInput && !targetInput.hasAttribute("data-i18n-placeholder")) {
                    targetInput.setAttribute("placeholder", translation);
                }
            }
        }
    });

    // 2. Dịch các placeholder được chỉ định riêng biệt (như ở trang Đặt bàn)
    document.querySelectorAll("[data-i18n-placeholder]").forEach(element => {
        const key = element.getAttribute("data-i18n-placeholder");
        if (dictionary[lang] && dictionary[lang][key]) {
            element.setAttribute("placeholder", dictionary[lang][key]);
        }
    });

    // 🌟 3. CẬP NHẬT ĐỘNG MENU VÀ TABS MÀ KHÔNG CẦN RELOAD TRANG
    if (typeof renderTabs === "function" && window.currentMenuData) {
        // A. Tìm xem nút danh mục nào đang active trước khi vẽ lại giao diện
        const activeTabButton = document.querySelector("#danh-muc-tabs .nav-link.active");
        let activeIndex = 0; // Mặc định nếu không tìm thấy sẽ là tab đầu tiên

        if (activeTabButton) {
            const allTabs = Array.from(document.querySelectorAll("#danh-muc-tabs .nav-link"));
            const foundIndex = allTabs.indexOf(activeTabButton);
            if (foundIndex !== -1) {
                activeIndex = foundIndex;
            }
        }

        // B. Vẽ lại toàn bộ chữ hiển thị trên các nút chọn danh mục (Tabs) theo ngôn ngữ mới
        renderTabs(window.currentMenuData);
        
        // C. Định vị lại nút active và tự động kích hoạt sự kiện click để nạp lại món ăn
        const newTabButtons = document.querySelectorAll("#danh-muc-tabs .nav-link");
        if (newTabButtons && newTabButtons.length > 0) {
            const targetIndex = activeIndex < newTabButtons.length ? activeIndex : 0;
            
            // Thêm bảo hiểm kiểm tra phần tử tồn tại trước khi click
            if (newTabButtons[targetIndex]) {
                newTabButtons[targetIndex].click();
            }
        }
    }

    // 4. Dịch động trang Lịch sử đơn hàng
   if (typeof window.loadOrderHistory === "function") {
        window.loadOrderHistory(); 
    }
    if (typeof window.loadProfile === "function") {
        window.loadProfile(); 
    }
    if (typeof window.loadAuthArea === "function") {
        window.loadAuthArea(); 
    }
}

document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("currentLanguage") || "vi";
    applyLanguage(savedLang);

    document.querySelectorAll(".lang-option").forEach(item => {
        item.addEventListener("click", (e) => {
            e.preventDefault();
            const targetLang = item.getAttribute("data-lang");
            applyLanguage(targetLang);
        });
    });
});